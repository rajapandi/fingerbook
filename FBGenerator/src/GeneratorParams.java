

public class GeneratorParams {

	public Integer topLimit;
	public Integer amountOfFingerbook;
	public Integer amountOfFingerPrintsLowLimit;
	public Integer amountOfFingerPrintsTopLimit;
	public String userName;
	
	public Integer getTopLimit() {
		return topLimit;
	}
	public void setTopLimit(Integer topLimit) {
		this.topLimit = topLimit;
	}
	public Integer getAmountOfFingerbook() {
		return amountOfFingerbook;
	}
	public void setAmountOfFingerbook(Integer amountOfFingerbook) {
		this.amountOfFingerbook = amountOfFingerbook;
	}
	public Integer getAmountOfFingerPrintsLowLimit() {
		return amountOfFingerPrintsLowLimit;
	}
	public void setAmountOfFingerPrintsLowLimit(Integer amountOfFingerPrintsLowLimit) {
		this.amountOfFingerPrintsLowLimit = amountOfFingerPrintsLowLimit;
	}
	public Integer getAmountOfFingerPrintsTopLimit() {
		return amountOfFingerPrintsTopLimit;
	}
	public void setAmountOfFingerPrintsTopLimit(Integer amountOfFingerPrintsTopLimit) {
		this.amountOfFingerPrintsTopLimit = amountOfFingerPrintsTopLimit;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
}
